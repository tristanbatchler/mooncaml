(* bmp2ml.ml *)
(* Convert BMP images to OCaml modules with matching names. *)

let terrain_of_rgb r g b =
  match r, g, b with
  | 0, 255, 0 -> "Grass"
  | 139, 69, 19 -> "Dirt"
  | 0, 0, 0 -> "Wall"
  | 255, 0, 255 -> "OutOfBounds"
  | _ -> "OutOfBounds"
;;

let write_ml_file ~output_dir ~filename ~image =
  let width = image.Image.width in
  let height = image.Image.height in
  let ml_filename =
    Filename.concat output_dir (Filename.chop_extension (Filename.basename filename) ^ ".ml")
  in
  let oc = open_out ml_filename in
  Fun.protect
    ~finally:(fun () -> close_out_noerr oc)
    (fun () ->
       Printf.fprintf oc "(* Auto-generated by bmp2ml from %s. Do not edit by hand. *)\n" filename;
       Printf.fprintf oc "open Mooncaml_shared.Terrain\n\n";
       Printf.fprintf oc "let width = %d\n" width;
       Printf.fprintf oc "let height = %d\n" height;
       Printf.fprintf oc "let pixels : Mooncaml_shared.Terrain.t array array = [|\n";
       for y = 0 to height - 1 do
         Printf.fprintf oc "  [|";
         for x = 0 to width - 1 do
           Image.read_rgb image x y (fun r g b ->
             let terrain = terrain_of_rgb r g b in
             Printf.fprintf oc "%s%s" terrain (if x < width - 1 then "; " else ""))
         done;
         Printf.fprintf oc "|]%s\n" (if y < height - 1 then ";" else "")
       done;
       Printf.fprintf oc "|]\n")
;;

let process ~output_dir filename =
  let image = ImageLib_unix.openfile filename in
  write_ml_file ~output_dir ~filename ~image;
  Printf.printf
    "Generated %s\n%!"
    (Filename.concat output_dir (Filename.chop_extension (Filename.basename filename) ^ ".ml"))
;;

let () =
  let input_dir = ref "data/maps" in
  let output_dir = ref "lib/server/maps" in
  let specs =
    [ "--input-dir", Arg.Set_string input_dir, "Directory containing .bmp files"
    ; "--output-dir", Arg.Set_string output_dir, "Directory where .ml files are written"
    ]
  in
  Arg.parse specs (fun _ -> ()) "bmp2ml";
  Sys.readdir !input_dir
  |> Array.iter (fun file ->
    if Filename.check_suffix file ".bmp"
    then (
      let filename = Filename.concat !input_dir file in
      process ~output_dir:!output_dir filename))
;;
